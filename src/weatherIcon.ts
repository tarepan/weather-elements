// 2: Thunderstorm
// 3: Drizzle (éœ§)
// 5: rain
// 6: snow
// 7: Atmosphere ...?
// 800: sunny
// 801: sunny cloudy
// 802-4: cloud
export function weatherIcon(weatherID: number): SVGTemplateResult {
  const id = String(weatherID);
  if (id.length !== 3) {
    return svg``;
  }
  const type = parseInt(id[0]);
  const detail = parseInt(id[2]);
  switch (type) {
    case 2:
      return thunderstorm;
    case 3:
      return other;
    case 5:
      return rain;
    case 6:
      return snow;
    case 7:
      return other;
    case 8:
      return ((): SVGTemplateResult => {
        switch (detail) {
          case 0:
            return sunny;
          case 1:
            return sunnyCloudy;
          case 2:
            return cloud;
          case 3:
            return cloud;
          case 4:
            return cloud;
          default:
            return svg``;
        }
      })();
    default:
      return svg``;
  }
}
